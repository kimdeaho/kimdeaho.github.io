package com.orion.model;

public class NewProductDAO {
	private String n_num;
	private String n_name;
	private String n_kind;
	private int n_price;
	private String n_sub;
	private String n_img;
	public String getN_num() {
		return n_num;
	}
	public void setN_num(String n_num) {
		this.n_num = n_num;
	}
	public String getN_name() {
		return n_name;
	}
	public void setN_name(String n_name) {
		this.n_name = n_name;
	}
	public String getN_kind() {
		return n_kind;
	}
	public void setN_kind(String n_kind) {
		this.n_kind = n_kind;
	}
	public int getN_price() {
		return n_price;
	}
	public void setN_price(int n_price) {
		this.n_price = n_price;
	}
	public String getN_sub() {
		return n_sub;
	}
	public void setN_sub(String n_sub) {
		this.n_sub = n_sub;
	}
	public String getN_img() {
		return n_img;
	}
	public void setN_img(String n_img) {
		this.n_img = n_img;
	}
}